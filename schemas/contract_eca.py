"""
ECA (Event Correlation Agent) Contract

Defines how ECA listens to Kafka topics, detects anomalies,
generates tickets, and feeds mitigation tasks back to the system.
"""

from typing import Dict, List, Any, Optional
from dataclasses import dataclass
from datetime import datetime
from enum import Enum
import uuid


# ============================================================================
# EVENT TYPES & SCHEMAS
# ============================================================================

class EventCategory(Enum):
    """Event categories ECA subscribes to."""
    SYSLOG_EVENT = "syslog-events"
    AGENT_ERROR = "agent-errors"
    DATA_UPDATE = "data-updates"
    PERFORMANCE_ALERT = "performance-alerts"
    SECURITY_AUDIT = "security-audits"


class EventSeverity(Enum):
    """Severity levels for events."""
    INFO = "info"
    WARNING = "warning"
    ERROR = "error"
    CRITICAL = "critical"


class AnomalyType(Enum):
    """Types of anomalies ECA can detect."""
    MCP_TIMEOUT = "mcp_timeout"
    MCP_FAILURE = "mcp_failure"
    MISSING_DATA = "missing_data"
    DATA_QUALITY_ISSUE = "data_quality_issue"
    SPECIALIST_CRASH = "specialist_crash"
    CREDENTIAL_DENIED = "credential_denied"
    RATE_LIMIT_EXCEEDED = "rate_limit_exceeded"
    LATENCY_SPIKE = "latency_spike"
    INCONSISTENT_DATA = "inconsistent_data"


@dataclass
class KafkaEvent:
    """
    Event from Kafka that ECA processes.
    
    Format: JSON serialized to Kafka topic
    """
    
    event_id: str
    timestamp: float
    category: str                     # e.g., "agent-errors"
    severity: str                     # e.g., "error"
    
    # Source information
    source_agent: str                 # e.g., "geological_scout_001"
    job_id: Optional[str]             # EJMS job ID if applicable
    trace_id: Optional[str]           # OpenTelemetry trace ID
    request_id: Optional[str]         # User request ID
    
    # Event content
    message: str
    details: Dict[str, Any]           # Event-specific details
    
    # Metadata
    processed: bool = False           # Has ECA processed this?
    anomaly_detected: bool = False    # Did ECA flag as anomaly?


@dataclass
class DetectedAnomaly:
    """
    Anomaly detected by ECA's rule engine.
    """
    
    anomaly_id: str
    timestamp: float
    anomaly_type: str                 # e.g., "mcp_timeout"
    severity: str                     # e.g., "critical"
    
    # Root cause
    source_event_id: str              # Kafka event that triggered detection
    root_cause: str                   # Human-readable description
    affected_component: str           # e.g., "geological_mcp"
    affected_job: Optional[str]
    
    # Remediation
    auto_remediation_possible: bool
    suggested_remediation: List[str]  # e.g., ["retry_with_backoff", "failover_to_cached_data"]
    
    # Logging
    ticket_generated: bool = False
    ticket_id: Optional[str] = None


@dataclass
class MitigationTicket:
    """
    Ticket generated by ECA for sub-tasks and remediation.
    """
    
    ticket_id: str
    ticket_type: str                  # "retry", "fallback", "escalate", "refresh_data"
    priority: str                     # "low", "medium", "high", "critical"
    
    # Task definition
    title: str
    description: str
    related_anomaly_id: str
    related_job_id: Optional[str]
    
    # Execution
    target_agent: Optional[str]       # Which agent should handle this
    execution_steps: List[str]        # Procedural steps
    
    # Metadata
    created_at: float
    execute_by: Optional[float]       # Deadline or auto-execute time
    status: str = "pending"           # "pending", "assigned", "executing", "completed", "failed"


