# TICKET GENERATION
# ============================================================================

class TicketGenerator:
    """
    Generates mitigation tickets based on detected anomalies.
    """

    @staticmethod
    def generate_ticket(anomaly: DetectedAnomaly) -> MitigationTicket:
        """
        Create a mitigation ticket for an anomaly.
        
        Maps anomaly type to ticket type & execution steps.
        """
        
        ticket_id = f"TKT_{uuid.uuid4().hex[:8]}"
        
        if anomaly.anomaly_type == AnomalyType.MCP_TIMEOUT.value:
            return TicketGenerator._generate_timeout_ticket(ticket_id, anomaly)
        
        elif anomaly.anomaly_type == AnomalyType.DATA_QUALITY_ISSUE.value:
            return TicketGenerator._generate_data_quality_ticket(ticket_id, anomaly)
        
        elif anomaly.anomaly_type == AnomalyType.CREDENTIAL_DENIED.value:
            return TicketGenerator._generate_credential_ticket(ticket_id, anomaly)
        
        elif anomaly.anomaly_type == AnomalyType.LATENCY_SPIKE.value:
            return TicketGenerator._generate_latency_ticket(ticket_id, anomaly)
        
        else:
            return TicketGenerator._generate_generic_ticket(ticket_id, anomaly)

    @staticmethod
    def _generate_timeout_ticket(ticket_id: str, anomaly: DetectedAnomaly) -> MitigationTicket:
        """Generate ticket for MCP timeout."""
        return MitigationTicket(
            ticket_id=ticket_id,
            ticket_type="retry",
            priority="high",
            title=f"Retry MCP query: {anomaly.affected_component}",
            description=f"MCP tool timed out. Retry with exponential backoff.\nAnomaly: {anomaly.root_cause}",
            related_anomaly_id=anomaly.anomaly_id,
            related_job_id=anomaly.affected_job,
            target_agent="orchestrator",  # Send back to control plane
            execution_steps=[
                "1. Log timeout event",
                "2. Wait 2 seconds (exponential backoff: 2^1)",
                "3. Retry MCP tool call",
                "4. On next failure: escalate to fallback data source"
            ],
            created_at=datetime.now().timestamp(),
            execute_by=datetime.now().timestamp() + 30  # Execute within 30 seconds
        )

    @staticmethod
    def _generate_data_quality_ticket(ticket_id: str, anomaly: DetectedAnomaly) -> MitigationTicket:
        """Generate ticket for data quality issue."""
        return MitigationTicket(
            ticket_id=ticket_id,
            ticket_type="escalate",
            priority="medium",
            title=f"Data quality issue: {anomaly.affected_component}",
            description=f"Manual review required.\n{anomaly.root_cause}",
            related_anomaly_id=anomaly.anomaly_id,
            related_job_id=anomaly.affected_job,
            target_agent=None,  # Manual intervention
            execution_steps=[
                "1. Review raw data from source",
                "2. Validate against schema",
                "3. Check for outliers or malformed records",
                "4. Decide: proceed with warnings or re-execute collection"
            ],
            created_at=datetime.now().timestamp(),
            execute_by=datetime.now().timestamp() + 900  # Execute within 15 minutes
        )

    @staticmethod
    def _generate_credential_ticket(ticket_id: str, anomaly: DetectedAnomaly) -> MitigationTicket:
        """Generate ticket for credential denied."""
        return MitigationTicket(
            ticket_id=ticket_id,
            ticket_type="escalate",
            priority="critical",
            title="Security: Credential access denied",
            description=f"ACS rejected credential request.\n{anomaly.root_cause}",
            related_anomaly_id=anomaly.anomaly_id,
            related_job_id=anomaly.affected_job,
            target_agent=None,  # Requires human admin
            execution_steps=[
                "1. Review ACS audit logs",
                "2. Check agent permissions/role",
                "3. Verify MCP server is properly registered",
                "4. If legitimate: update IAM policy and retry"
            ],
            created_at=datetime.now().timestamp(),
            execute_by=None  # Manual escalation
        )

    @staticmethod
    def _generate_latency_ticket(ticket_id: str, anomaly: DetectedAnomaly) -> MitigationTicket:
        """Generate ticket for latency spike."""
        return MitigationTicket(
            ticket_id=ticket_id,
            ticket_type="investigate",
            priority="medium",
            title=f"Latency spike: {anomaly.affected_component}",
            description=f"Execution time exceeded threshold.\n{anomaly.root_cause}",
            related_anomaly_id=anomaly.anomaly_id,
            related_job_id=anomaly.affected_job,
            target_agent="monitoring",
            execution_steps=[
                "1. Check system resource utilization (CPU, memory)",
                "2. Check MCP server responsiveness",
                "3. Review network latency metrics",
                "4. If persistent: trigger auto-scaling or alert ops"
            ],
            created_at=datetime.now().timestamp(),
            execute_by=datetime.now().timestamp() + 60  # Investigate within 60 seconds
        )

    @staticmethod
    def _generate_generic_ticket(ticket_id: str, anomaly: DetectedAnomaly) -> MitigationTicket:
        """Generate generic ticket for unknown anomaly."""
        return MitigationTicket(
            ticket_id=ticket_id,
            ticket_type="escalate",
            priority="high",
            title=f"Anomaly detected: {anomaly.anomaly_type}",
            description=f"Unknown anomaly type.\n{anomaly.root_cause}",
            related_anomaly_id=anomaly.anomaly_id,
            related_job_id=anomaly.affected_job,
            target_agent=None,
            execution_steps=[
                "1. Review anomaly details in logs",
                "2. Understand root cause",
                "3. Determine if auto-remediation or manual intervention needed"
            ],
            created_at=datetime.now().timestamp()
        )


