# EVENT ROUTING TO IO AGENT
# ============================================================================

class EventRoutingContract:
    """
    Defines how ECA routes mitigation tickets back to IO Agent.
    """

    @staticmethod
    def route_ticket_to_io_agent(
        ticket: MitigationTicket,
        user_request_id: str
    ) -> Dict[str, Any]:
        """
        Send ticket to IO Agent as a system notification/mitigation prompt.
        
        The IO Agent can then:
        1. Add to response context
        2. Prompt user for action
        3. Auto-retry if benign
        4. Escalate if critical
        
        Message format for IO Agent:
        """
        return {
            "type": "mitigation_prompt",
            "severity": ticket.priority,
            "user_request_id": user_request_id,
            "ticket_id": ticket.ticket_id,
            "title": ticket.title,
            "description": ticket.description,
            "message": f"""
            During processing, an issue was detected:
            
            [{ticket.ticket_id}] {ticket.title}
            Priority: {ticket.priority}
            
            {ticket.description}
            
            Recommended Actions:
            {chr(10).join(f'  - {step}' for step in ticket.execution_steps[:3])}
            
            Status: {ticket.status}
            """,
            "auto_remediation": ticket.target_agent is not None,
            "requires_user_action": ticket.target_agent is None
        }


