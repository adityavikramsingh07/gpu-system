# ANOMALY DETECTION RULES
# ============================================================================

class AnomalyDetectionRules:
    """
    Rule engine for detecting anomalies in events.
    """

    @staticmethod
    def apply_rules(event: KafkaEvent) -> Optional[DetectedAnomaly]:
        """
        Apply detection rules to incoming event.
        
        Returns:
            DetectedAnomaly if anomaly detected, None otherwise
        """
        
        # Rule 1: MCP Tool Timeout
        if event.category == "agent-errors" and "timeout" in event.message.lower():
            return AnomalyDetectionRules._create_mcp_timeout_anomaly(event)
        
        # Rule 2: Repeated Failures from Same MCP
        # (Would track in-memory state here)
        
        # Rule 3: Data Quality Issue
        if event.category == "data-updates" and event.details.get("anomaly_flag"):
            return AnomalyDetectionRules._create_data_quality_anomaly(event)
        
        # Rule 4: Credential Denied
        if "credential" in event.message.lower() and "denied" in event.message.lower():
            return AnomalyDetectionRules._create_credential_denied_anomaly(event)
        
        # Rule 5: Latency Spike (> 10s)
        if event.details.get("execution_time_ms", 0) > 10000:
            return AnomalyDetectionRules._create_latency_spike_anomaly(event)
        
        # No anomaly detected
        return None

    @staticmethod
    def _create_mcp_timeout_anomaly(event: KafkaEvent) -> DetectedAnomaly:
        """Create MCP timeout anomaly."""
        return DetectedAnomaly(
            anomaly_id=str(uuid.uuid4()),
            timestamp=datetime.now().timestamp(),
            anomaly_type=AnomalyType.MCP_TIMEOUT.value,
            severity=EventSeverity.WARNING.value,
            source_event_id=event.event_id,
            root_cause=f"MCP tool {event.details.get('mcp_tool')} timed out",
            affected_component=event.details.get("mcp_server", "unknown"),
            affected_job=event.job_id,
            auto_remediation_possible=True,
            suggested_remediation=["retry_with_exponential_backoff", "failover_to_cache", "escalate_to_human"]
        )

    @staticmethod
    def _create_data_quality_anomaly(event: KafkaEvent) -> DetectedAnomaly:
        """Create data quality anomaly."""
        return DetectedAnomaly(
            anomaly_id=str(uuid.uuid4()),
            timestamp=datetime.now().timestamp(),
            anomaly_type=AnomalyType.DATA_QUALITY_ISSUE.value,
            severity=EventSeverity.WARNING.value,
            source_event_id=event.event_id,
            root_cause=f"Data quality issue in {event.details.get('dataset')}",
            affected_component=event.source_agent,
            affected_job=event.job_id,
            auto_remediation_possible=False,
            suggested_remediation=["manual_review", "re-execute_data_collection", "escalate_to_human"]
        )

    @staticmethod
    def _create_credential_denied_anomaly(event: KafkaEvent) -> DetectedAnomaly:
        """Create credential denied anomaly."""
        return DetectedAnomaly(
            anomaly_id=str(uuid.uuid4()),
            timestamp=datetime.now().timestamp(),
            anomaly_type=AnomalyType.CREDENTIAL_DENIED.value,
            severity=EventSeverity.CRITICAL.value,
            source_event_id=event.event_id,
            root_cause="ACS denied credential request (permission denied)",
            affected_component="ACS",
            affected_job=event.job_id,
            auto_remediation_possible=False,
            suggested_remediation=["escalate_to_admin", "check_acs_policies", "audit_access"]
        )

    @staticmethod
    def _create_latency_spike_anomaly(event: KafkaEvent) -> DetectedAnomaly:
        """Create latency spike anomaly."""
        return DetectedAnomaly(
            anomaly_id=str(uuid.uuid4()),
            timestamp=datetime.now().timestamp(),
            anomaly_type=AnomalyType.LATENCY_SPIKE.value,
            severity=EventSeverity.WARNING.value,
            source_event_id=event.event_id,
            root_cause=f"Latency spike: {event.details.get('execution_time_ms')}ms (threshold: 10000ms)",
            affected_component=event.source_agent,
            affected_job=event.job_id,
            auto_remediation_possible=True,
            suggested_remediation=["increase_timeout", "check_system_resources", "investigate_mcp_server"]
        )


